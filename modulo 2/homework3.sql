--Fazer um RIGHT OUTER JOIN entre tabelas:
--Pessoa e Contato
SELECT P.NOME,
  C.NUMERO
FROM PESSOA P
  RIGHT JOIN CONTATO C ON (P.ID_PESSOA = C.ID_PESSOA);

--Pessoa, PESSOA_X_PESSOA_ENDERECO e Endereco_Pessoa
SELECT P.NOME,
  E.LOGRADOURO,
  E.NUMERO,
  E.CIDADE,
  E.ESTADO
FROM PESSOA P
  RIGHT JOIN PESSOA_X_PESSOA_ENDERECO PXPE ON (P.ID_PESSOA = PXPE.ID_PESSOA)
  RIGHT JOIN ENDERECO_PESSOA E ON (PXPE.ID_ENDERECO = E.ID_ENDERECO);

--Todas as tabelas (começando por pessoa)
