--Utilizando o EXISTS, selecione as pessoas que tem endereço
SELECT P.NOME
FROM PESSOA p
WHERE EXISTS (
    SELECT E.ID_ENDERECO
    FROM PESSOA_X_PESSOA_ENDERECO e
    WHERE P.ID_PESSOA = E.ID_PESSOA
  );

